import 'package:flutter/material.dart';

void main() {
  // Demonstration of int data type
  int age = 26;

  // Demonstration of double data type
  double temperature = 25.5;

  // Demonstration of String data type
  String name = "Kharavho Ndou";

  // Demonstration of List data type
  List<int> numbers = [1, 2, 3, 4, 5];

  // Demonstration of Map data type
  Map<String, String> capitalCities = {
    "South Africa": "Johannesburg",
    "Kenya": "Nairobi",
    "Tanzania": "Dodoma",
  };

  // Printing out the values
  print("Age: $age");
  print("Temperature: $temperature");
  print("Name: $name");
  print("Numbers: $numbers");
  print("Capital Cities: $capitalCities");
}
